const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "21d83544";

export default function App() {
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);
  const { movies, isLoading, error } = useMovies(query);

  const [watched, setWatched] = useLocalStorageState([], "watched");

  function handleRemoveWatchedMovie(idToRemove) {
    setWatched((watched) =>
      watched.filter((movie) => movie.imdbID !== idToRemove)
    );
  }

  function handleCloseSelected() {
    setSelectedId(null);
  }

  function handleSelectedId(id) {
    setSelectedId((selectedId) => (selectedId === id ? null : id));
  }

  function handleSetWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  return (
    <>
      <Navbar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </Navbar>
      <Main>
        <Box>
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSetSelectedId={handleSelectedId} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <SelectedMovie
              onCloseSelected={handleCloseSelected}
              selectedId={selectedId}
              // onSetError={setError}
              onSetWatched={handleSetWatched}
              watchedMovies={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onRemoveWatchedMovie={handleRemoveWatchedMovie}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

function SelectedMovie({
  onCloseSelected,
  selectedId,
  onSetError,
  onSetWatched,
  watchedMovies,
}) {
  const [movie, setMovie] = useState({});
  const [isLoading, setIsLoading] = useState(false);
  const [userRating, setUserRating] = useState("");

  const countRef = useRef(0);

  const isWatched = watchedMovies
    .map((movie) => movie.imdbID)
    .includes(selectedId);

  const watchedUserRating = watchedMovies.find(
    (movie) => movie.imdbID === selectedId
  )?.userRating;

  const {
    Title: title,
    Poster: poster,
    Runtime: runtime,
    Released: released,
    Genre: genre,
    Plot: plot,
    Actors: actors,
    Director: director,
    imdbRating,
  } = movie;

  useEffect(
    function () {
      if (userRating) countRef.current++;
    },
    [userRating]
  );

  useEffect(
    function () {
      const fetchMovie = async () => {
        try {
          setIsLoading(true);

          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&i=${selectedId}`
          );

          if (!res.ok) throw new Error("Something went wrong!");

          const data = await res.json();

          setMovie(data);
          setIsLoading(false);
        } catch (err) {
          onSetError(err.message);
        }
      };

      if (!selectedId) return;

      fetchMovie();
    },
    [selectedId, onSetError]
  );

  useEffect(
    function () {
      if (!title) return;
      document.title = `Movie | ${title}`;

      return function () {
        document.title = "usePopcorn";
      };
    },
    [title]
  );

  useKey("Escape", onCloseSelected);

  function handleAdd() {
    const newWatchedMovie = {
      imdbID: selectedId,
      title,
      poster,
      runtime: Number(runtime.split(" ").at(0)),
      imdbRating: Number(imdbRating),
      userRating: userRating,
      countRatingDecisions: countRef.current,
    };

    onSetWatched(newWatchedMovie);
    onCloseSelected();
  }

  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={() => onCloseSelected()}>
              &larr;
            </button>
            <img src={poster} alt={title} />
            <div className="details-overview">
              <h2>{title}</h2>
              <p>
                {released} ¬∑ {runtime}
              </p>
              <p>{genre}</p>
              <p>‚≠ê {imdbRating} IMDb rating</p>
            </div>
          </header>

          <section>
            {isWatched ? (
              <p>You rated movie with {watchedUserRating} ‚≠ê</p>
            ) : (
              <div className="rating">
                <StarRating
                  size={24}
                  maxRating={10}
                  onMovieRating={setUserRating}
                />
                {userRating && (
                  <button className="btn-add" onClick={handleAdd}>
                    + Add to list
                  </button>
                )}
              </div>
            )}
            <p>
              <em>{plot}</em>
            </p>
            <p>Starring {actors}</p>
            <p>Directed by {director}</p>
          </section>
        </>
      )}
    </div>
  );
}

